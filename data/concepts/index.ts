export function getConceptByTag(tag: string): Concept | undefined {
  return CONCEPTS.find(c => c.tag === tag);
}

export function getConceptsByDomain(domain: string): Concept[] {
  return CONCEPTS.filter(c => c.domain === domain);
}

export function getConceptsByCategory(category: string): Concept[] {
  return CONCEPTS.filter(c => c.category === category);
}

export function getRelatedConcepts(concept: Concept): Concept[] {
  if (!concept.relatedConcepts) return [];
  return concept.relatedConcepts
    .map(tag => getConceptByTag(tag))
    .filter((c): c is Concept => Boolean(c));
}
